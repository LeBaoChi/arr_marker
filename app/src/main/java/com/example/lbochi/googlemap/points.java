public class points {

    private String[] lng;
    private String[] lat;
    private String[] name;

    public String[] getLng() {
        return lng;
    }

    public String[] getLat() {
        return lat;
    }
    public String[] getName() {
        return name;
    }

    public void setLng(String[] lng) {
        this.lng = lng;
    }

    public void setLat(String[] lat) {
        this.lat = lat;
    }
    public void setName(String[] name) {
        this.name = name;
    }

}
